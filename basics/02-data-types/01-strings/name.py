# Strings - A string is a series of characters. Anything inside quotes is considered a string in Python and you can use single or double quotes around your strings like this:
# eg - "This is a string" or 'This is also a string'
# This flexibility allows you to use quotes and apostrophes within your strings

# Changing Case in a String with Methods
# title() method changes each work to title case, where each word begins with a capital letter

name = "ada lovelace"
print(name.title())

# upper() and lower() - You can change a string to all uppercase or all lowercase letters like this:

new_name = "Your Name"
print(new_name.upper())
print(new_name.lower())
