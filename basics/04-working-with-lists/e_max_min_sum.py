# Simple Statistics with a List of Numbers
# In Python you can easily find the minimum, maximum, and sum of a list of numbers.

digits = [1, 2, 3, 4, 5, 6, 7, 8, 9, 0]
min_digits = min(digits)
max_digits = max(digits)
sum_digits = sum(digits)
print(f"In List maximum num is {max_digits}, minimum num is {min_digits} and sum of these num is {sum_digits}")

